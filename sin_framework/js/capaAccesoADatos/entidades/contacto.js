class Contacto{
    constructor(id,nombre,correo,telefono,asunto,mensaje,terminos,preferenciaContacto,fechaCreacion,fechaEdicion){
        this.id = id;
        this.nombre = nombre;
        this.correo = correo;
        this.telefono = telefono;
        this.asunto = asunto;
        this.mensaje = mensaje;
        this.terminos = terminos;
        this.preferenciaContacto = preferenciaContacto;
        this.fechaCreacion = fechaCreacion;
        this.fechaEdicion = fechaEdicion;
    }
}